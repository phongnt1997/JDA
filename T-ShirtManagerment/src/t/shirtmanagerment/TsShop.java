    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            if (stub.SaveList(data) == true) {
                JOptionPane.showMessageDialog(this, "Saved");
            }
            else JOptionPane.showMessageDialog(this, "Cannot save");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_btnSaveActionPerformed

    private void tblTsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTsMouseClicked
         // TODO add your handling code here:
         int selectedRow = tblTs.getSelectedRow();
         txtCode.setText((String)(tblTs.getValueAt(selectedRow, 0)));
         txtName.setText((String)(tblTs.getValueAt(selectedRow, 1)));
         txtSize.setText((String)(tblTs.getValueAt(selectedRow, 2)));
         txtPrice.setText((String)(tblTs.getValueAt(selectedRow, 3)));
         txtCode.setEditable(false);
         addNew = false;
    }//GEN-LAST:event_tblTsMouseClicked


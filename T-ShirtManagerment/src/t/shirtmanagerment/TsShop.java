    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (addNew) {
            Vector v = new Vector();
            v.add(txtCode.getText().trim());
            v.add(txtName.getText().trim());
            v.add(txtSize.getText().trim());
            v.add(txtPrice.getText().trim());
            data.add(v);
        } else {
            int selectedRow = tblTs.getSelectedRow();
            tblTs.setValueAt(txtCode.getText(), selectedRow, 0);
            tblTs.setValueAt(txtName.getText(), selectedRow, 1);
            tblTs.setValueAt(txtSize.getText(), selectedRow, 2);
            tblTs.setValueAt(txtPrice.getText(), selectedRow, 3);
        }
    }//GEN-LAST:event_btnAddActionPerformed


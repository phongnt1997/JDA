    private void cbbFontActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbbFontActionPerformed
         // TODO add your handling code here:
         this.fontName = (String)cbbFont.getSelectedItem();
         this.font = new Font(fontName, fontStyle, fontSize);
         
    }//GEN-LAST:event_cbbFontActionPerformed

    private void cbbStyleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbbStyleActionPerformed
         // TODO add your handling code here:
         String str = (String) cbbStyle.getSelectedItem();
         if(str.equalsIgnoreCase("Bold")) fontStyle = Font.BOLD;
         else if(str.equalsIgnoreCase("Italic")) fontStyle = Font.ITALIC;
         else fontStyle = Font.PLAIN;
         this.font = new Font(fontName, fontStyle, fontSize);
    }//GEN-LAST:event_cbbStyleActionPerformed

    private void txtSizeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSizeActionPerformed
        // TODO add your handling code here:
        fontSize = Integer.parseInt(txtSize.getText().trim());
        font = new Font(fontName, fontStyle, fontSize);
        g.setFont(font);
    }//GEN-LAST:event_txtSizeActionPerformed

    private void txtSizeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtSizeFocusLost
        // TODO add your handling code here:
        fontSize = Integer.parseInt(txtSize.getText().trim());
        font = new Font(fontName, fontStyle, fontSize);
        g.setFont(font);
    }//GEN-LAST:event_txtSizeFocusLost

    private void pnlDrawMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnlDrawMouseClicked
        // TODO add your handling code here:
        int x = evt.getX();
        int y = evt.getY();
        g.setFont(font);
        g.setColor(color);
        drawText = txtContent.getText();
        g.drawString(drawText, x, y);
        showproperties();
    }//GEN-LAST:event_pnlDrawMouseClicked

    private void showproperties() {
        txtProperties.setText("");
        txtProperties.append("Family: " + font.getFamily() + "\n");
        txtProperties.append("Name: " + font.getFontName() + "\n");
        txtProperties.append("Style: " + font.getStyle() + "\n");
        txtProperties.append("Size: " + font.getSize() + "\n");
    }

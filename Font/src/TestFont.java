    private void loadFont() {
        GraphicsEnvironment env = GraphicsEnvironment.getLocalGraphicsEnvironment();
        Font[] fonts = env.getAllFonts();
        this.cbbFont.removeAllItems();
        String fname = "";
        for (Font f : fonts) {
            fname = f.getFontName();
            this.cbbFont.addItem(fname);
        }
    }

